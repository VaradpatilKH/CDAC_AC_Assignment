public class Q2_ArithmeticAndCasting {
    public static void main(String[] args) {
        int a = 15;
        double b = 4.0;

        System.out.println("Addition         : " + (a + b));
        System.out.println("Division         : " + (a / b));
        System.out.println("Division (int cast): " + (int)(a / b));
    }
}
